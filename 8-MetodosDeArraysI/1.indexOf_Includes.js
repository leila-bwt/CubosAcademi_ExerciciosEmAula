//indexOf:
// Encontra um item dentro de um array, retornando o índice
// da primeira ocorrência do item procurado
//Quando não encontrado retorna -1
const lista = ["Eu", "Tu", "Eles"];
const indice = lista.indexOf("Tu");
console.log(indice);


